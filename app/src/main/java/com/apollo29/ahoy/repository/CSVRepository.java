package com.apollo29.ahoy.repository;

import com.opencsv.CSVWriter;
import com.orhanobut.logger.Logger;

import java.io.IOException;
import java.io.OutputStream;
import java.io.PrintWriter;
import java.util.List;

public class CSVRepository {

    public static void csv(OutputStream file, List<String[]> data) throws IOException {
        Logger.d("csv output");
        Logger.d(data);
        CSVWriter writer=null;
        writer = new CSVWriter(new PrintWriter(file), ',');
        writer.writeAll(data);
        writer.writeNext(new String[]{"List generated by Ahoy! App -"}); // todo add link to app
        writer.close();
    }
}
